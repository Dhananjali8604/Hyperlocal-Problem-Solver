export type TranslationKey = keyof typeof translations.en;

export const useTranslation = (langCode: string | undefined) => {
  const t = (key: TranslationKey) => {
    const lang = (langCode && langCode in translations) ? langCode as keyof typeof translations : 'en';
    return translations[lang][key] || translations['en'][key];
  };
  return { t };
};
